//        Example
//        Input : num = 11
//        Output : Positive (number bada h to + no chuta h to -)
//Step 1. Start
//        Step 2. Insert the number.
//        Step 3. If the number is greater than Zero then print “The number is Positive”
//        Step 4: If the number is smaller than zero, then print, “The number is Negative”
//        Step 5. Else print, “The number is Zero”
//        Step 6. Stop
public class Positive_or_Negative{
    public static void main(String[] args) {
        int num=0;
        if (num >0)
            System.out.println(num + ":positive");
        else if(num<0)
            System.out.println(num + ":negative");
        else
            System.out.println(num+":zero");
    }
}
