//Example
//        Input : Number1 = 12 , Number2 = 15
//        Output : 54
public class Sum_of_the_Numbers_in_a_Given_Range {
    public static void main(String[] args) {
        int a=2;
        int b=5;
        int sum=0;
        for (int i=a; i<=b; i++){
            sum=sum+i;
            System.out.println("The sum is :"+sum);
        }

    }
}
